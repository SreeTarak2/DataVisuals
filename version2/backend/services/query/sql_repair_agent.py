"""
sql_repair_agent.py
====================
Self-healing SQL agent for Signal — handles every DuckDB error class
observed in production logs.

Each fix is its own function so it can be tested and swapped independently.

Errors this agent resolves (from live backend logs):
  E-01  Schema parser regex mismatch — column whitelist always empty →
        model hallucinates columns → every retry fails with the same error
  E-02  "syntax error at end of input" — reasoning trace too long, SQL
        output gets cut off before it's complete
  E-03  "syntax error at or near ')'" with COUNT(*) AS) — model writes
        COUNT(*) AS with no alias; executor wraps in subquery → double paren
  E-04  "Set operations can only apply to expressions with the same number
        of result columns" — UNION branches have different SELECT lists
  E-05  Generic LLM repair — targeted prompt per error class, not one-size

Architecture:
    DuckDB error
        → classify_sql_error()          [E-01 to E-04 + generic]
        → rule-based repair attempt     [fast, no LLM call]
            if repairs the SQL → execute immediately
            if not → build_targeted_repair_prompt() → LLM call → execute
        → if still failing → simplified fallback SQL
"""

from __future__ import annotations

import logging
import re
from dataclasses import dataclass, field
from enum import Enum
from typing import Optional

import polars as pl

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# ERROR TAXONOMY
# ---------------------------------------------------------------------------


class SQLErrorType(Enum):
    TRUNCATED = "truncated"  # E-02: syntax error at end of input
    MISSING_ALIAS = "missing_alias"  # E-03: AS) → alias missing
    UNION_MISMATCH = "union_mismatch"  # E-04: UNION column count mismatch
    COLUMN_NOT_FOUND = "column_not_found"  # hallucinated column name
    WINDOW_IN_AGGREGATE = "window_in_aggregate"  # aggregate cannot contain window fn
    SCHEMA_PARSE_FAIL = "schema_parse_fail"  # E-01: whitelist was empty
    GENERIC = "generic"  # anything else


@dataclass
class SQLErrorClass:
    error_type: SQLErrorType
    original_error: str
    repair_hint: str  # human-readable, injected into repair prompt
    can_rule_repair: bool = False  # True when a rule-based fix is possible


# ---------------------------------------------------------------------------
# [FIX E-01]  Column extractor that reads directly from the Polars DataFrame.
# ---------------------------------------------------------------------------


def extract_columns_from_df(df: pl.DataFrame) -> list[str]:
    r"""
    Extract exact column names directly from the Polars DataFrame.

    This is the preferred path for get_sql_generation_prompt(allowed_columns=...)
    because it is 100% reliable — no regex, no string parsing, no fallback.

    Previously: _parse_columns_from_schema() tried to regex-parse the schema
    string generated by _get_column_schema().  The backtick format
    ('  - `col` (Type) — Example: val') did not match the old regex
    r'^\s*"?([A-Za-z_][A-Za-z0-9_]*)\"?\s' (designed for quoted format).
    Result: empty whitelist → model hallucination → every retry fails with
    the same schema parse warning in the logs.

    Usage in executor.py:
        columns = extract_columns_from_df(df)
        prompt = get_sql_generation_prompt(
            ...,
            allowed_columns=columns,   # <-- pass directly, skip _parse_columns
        )
    """
    skip_suffixes = ("_id", "_key", "_uuid", "_pk")
    cols = []
    for col in df.columns:
        col_lower = col.lower()
        if any(col_lower.endswith(s) for s in skip_suffixes):
            logger.debug(f"[col_extractor] Skipping ID column: {col}")
            continue
        if df[col].n_unique() <= 1:
            logger.debug(f"[col_extractor] Skipping constant column: {col}")
            continue
        cols.append(col)

    logger.info(f"[col_extractor] Extracted {len(cols)} usable columns from DataFrame")
    return cols


def build_column_whitelist_block(columns: list[str]) -> str:
    """
    Build the SQL prompt whitelist block from a verified column list.
    Replaces the fragile _parse_columns_from_schema → whitelist_block path.
    """
    if not columns:
        return (
            "## COLUMN NAMES\n"
            "Use ONLY columns from DATASET SCHEMA below.\n"
            "If a required column is missing: SELECT 'Cannot answer: column not found' AS error_message\n"
        )
    col_lines = "\n".join(f"  - {c}" for c in columns)
    return (
        "## EXACT COLUMN NAMES — ONLY THESE ARE VALID IN YOUR SQL\n"
        "Any column name NOT in this list will cause a DuckDB BinderError.\n"
        f"{col_lines}\n\n"
        "IF A REQUIRED COLUMN IS MISSING FROM THIS LIST:\n"
        "  SELECT 'Cannot answer: required column not available' AS error_message\n"
    )


# ---------------------------------------------------------------------------
# [FIX E-02, E-03, E-04]  Error classifier
# Maps a DuckDB error message to an SQLErrorClass with repair metadata.
# ---------------------------------------------------------------------------


def classify_sql_error(error_msg: str, sql: str = "") -> SQLErrorClass:
    """
    Classify a DuckDB error message into an actionable SQLErrorClass.

    Parameters
    ----------
    error_msg : str
        The error string from DuckDB (e.g., "Parser Error: syntax error at end of input").
    sql : str
        The SQL that caused the error (used for pattern-level diagnosis).

    Returns
    -------
    SQLErrorClass
        Contains the error_type enum, the original message, a repair_hint for
        the LLM prompt, and can_rule_repair indicating whether a code-level fix
        is possible without an LLM call.
    """
    msg_lower = error_msg.lower()

    # E-02: Truncated SQL — reasoning trace exhausted token budget
    if "syntax error at end of input" in msg_lower:
        return SQLErrorClass(
            error_type=SQLErrorType.TRUNCATED,
            original_error=error_msg,
            repair_hint=(
                "The SQL is incomplete — it was cut off before finishing. "
                "Check that every opening parenthesis has a closing one, "
                "every CTE WITH block has a final SELECT, and every CASE has an END. "
                "Output ONLY a complete, self-contained SQL query."
            ),
            can_rule_repair=True,
        )

    # E-03: Missing alias — model wrote "COUNT(*) AS)" with no alias name
    if "syntax error at or near" in msg_lower and sql:
        if re.search(r"\bAS\s*\)", sql, re.IGNORECASE):
            return SQLErrorClass(
                error_type=SQLErrorType.MISSING_ALIAS,
                original_error=error_msg,
                repair_hint=(
                    "Every 'AS' keyword must be followed by an alias name. "
                    "Found 'AS)' with no alias — replace with 'AS col_name)'. "
                    "Example: COUNT(*) AS cnt, SUM(revenue) AS total_revenue."
                ),
                can_rule_repair=True,
            )

    # E-04: UNION column count mismatch
    if (
        "set operations can only apply" in msg_lower
        or "same number of result columns" in msg_lower
    ):
        return SQLErrorClass(
            error_type=SQLErrorType.UNION_MISMATCH,
            original_error=error_msg,
            repair_hint=(
                "UNION branches must have the same number of columns with compatible types. "
                "Remove the UNION entirely and rewrite as a single SELECT with GROUP BY, "
                "or use a WITH clause (CTE) to combine results without UNION."
            ),
            can_rule_repair=False,
        )

    # Column not found — model hallucinated a column name
    if (
        "does not exist" in msg_lower
        or "column not found" in msg_lower
        or "binder error" in msg_lower
    ):
        bad_col_match = re.search(
            r'column["\s]+([A-Za-z_][A-Za-z0-9_]*)["\s]+does not exist',
            error_msg,
            re.IGNORECASE,
        )
        col_hint = f" (bad column: '{bad_col_match.group(1)}')" if bad_col_match else ""
        return SQLErrorClass(
            error_type=SQLErrorType.COLUMN_NOT_FOUND,
            original_error=error_msg,
            repair_hint=(
                f"A column name does not exist in the table{col_hint}. "
                "Use ONLY the column names listed in the EXACT COLUMN NAMES block above. "
                "Never invent or abbreviate column names."
            ),
            can_rule_repair=False,
        )

    # Window function inside aggregate
    if "window function" in msg_lower and "aggregate" in msg_lower:
        return SQLErrorClass(
            error_type=SQLErrorType.WINDOW_IN_AGGREGATE,
            original_error=error_msg,
            repair_hint=(
                "Window functions (ROW_NUMBER, RANK, LAG, etc.) cannot be nested inside "
                "aggregate functions (SUM, COUNT, etc.). "
                "Use a CTE: first compute the window function, then wrap it in an outer SELECT "
                "with the aggregate."
            ),
            can_rule_repair=False,
        )

    # Generic fallback
    return SQLErrorClass(
        error_type=SQLErrorType.GENERIC,
        original_error=error_msg,
        repair_hint=(
            f"DuckDB returned this error: {error_msg}. "
            "Fix the SQL so it runs successfully on DuckDB."
        ),
        can_rule_repair=False,
    )


# ---------------------------------------------------------------------------
# [FIX E-02]  Rule-based repair: truncated SQL
# ---------------------------------------------------------------------------


def repair_truncated_sql(sql: str) -> str:
    """
    Attempt to close an incomplete SQL query without an LLM call.

    Handles:
    - Unclosed parentheses: counts '(' vs ')' and appends the missing ')'s
    - Missing semicolon
    - Dangling AS keyword with no alias at end of string

    Returns the repaired SQL, or the original if no structural truncation is found.
    This is a best-effort rule repair — if DuckDB still rejects it, the LLM
    repair prompt is the next step.

    From the logs:
      "syntax error at end of input" on attempt 1 with 9,452-char reasoning trace —
      the model's output budget was consumed by reasoning, leaving the SQL cut off.
    """
    if not sql or not sql.strip():
        return sql

    repaired = sql.strip().rstrip(";")

    # Fix dangling AS at very end: "SELECT col AS" → "SELECT col AS col"
    dangling_as = re.search(r"\bAS\s*$", repaired, re.IGNORECASE)
    if dangling_as:
        repaired = repaired[: dangling_as.start()].rstrip()
        logger.debug("[repair_truncated] Removed dangling AS at end of SQL")

    # Close unclosed parentheses
    open_count = repaired.count("(")
    close_count = repaired.count(")")
    gap = open_count - close_count
    if gap > 0:
        repaired += ")" * gap
        logger.info(
            f"[repair_truncated] Added {gap} closing parenthesis(es) to incomplete SQL"
        )

    # Close unclosed string literals (single quote count must be even)
    if repaired.count("'") % 2 != 0:
        repaired += "'"
        logger.debug("[repair_truncated] Closed unclosed string literal")

    repaired = repaired.rstrip(";") + ";"
    return repaired


# ---------------------------------------------------------------------------
# [FIX E-03]  Rule-based repair: missing alias after AS
# ---------------------------------------------------------------------------


def repair_missing_alias(sql: str) -> str:
    """
    Fix 'AS)' patterns where the model wrote an AS keyword with no alias.

    From the logs:
        COUNT(*) AS) AS subquery LIMIT 1000
        ↑ DuckDB: syntax error at or near ")"

    This happens because the model writes "COUNT(*) AS" at the end of a line,
    the executor then wraps the whole thing in:
        SELECT * FROM (...) AS subquery LIMIT 1000
    producing:  COUNT(*) AS) AS subquery

    Rule: replace every 'expression AS)' with 'expression AS _alias_N)'
    using the position index to generate a unique alias per column.
    """
    if not sql:
        return sql

    alias_counter = [0]

    def _add_alias(match: re.Match) -> str:
        alias_counter[0] += 1
        expr = match.group(1).strip()
        fn_match = re.match(r"(\w+)\s*\(", expr)
        base = fn_match.group(1).lower() if fn_match else "col"
        alias = f"{base}_{alias_counter[0]}"
        logger.info(
            f"[repair_missing_alias] Fixed 'AS)' → 'AS {alias})' for expr: {expr[:40]}"
        )
        return f"{expr} AS {alias})"

    repaired = re.sub(
        r"([^,(\n]+?)\s+AS\s*\)",
        _add_alias,
        sql,
        flags=re.IGNORECASE,
    )

    if repaired != sql:
        logger.info("[repair_missing_alias] Rule-based alias repair applied")
    return repaired


# ---------------------------------------------------------------------------
# [FIX E-04]  Rule-based repair: UNION column count mismatch
# ---------------------------------------------------------------------------


def repair_union_mismatch(sql: str, columns: list[str]) -> str:
    """
    Replace a UNION query with a single GROUP BY query when branches have
    mismatched column counts — the most common production failure pattern.

    From the logs:
        "Set operations can only apply to expressions with the same number
         of result columns"
    DeepSeek V3.2 with high-effort reasoning tends to generate ambitious
    UNION queries that mix aggregated and non-aggregated branches.

    Strategy:
        If the SQL contains UNION (all/except/intersect), try to rewrite it
        as a simple SELECT ... GROUP BY using the first available numeric
        column and the first available categorical column.

    This is the last rule-based resort before the LLM repair prompt.
    """
    if not sql:
        return sql

    has_union = bool(re.search(r"\bUNION\b|\bUNION\s+ALL\b", sql, re.IGNORECASE))
    if not has_union:
        return sql

    logger.info("[repair_union_mismatch] UNION detected — attempting fallback rewrite")

    cat_col = next(
        (
            c
            for c in columns
            if not any(c.lower().endswith(s) for s in ("_id", "_key", "_uuid"))
        ),
        None,
    )
    num_col = next(
        (
            c
            for c in columns
            if c != cat_col and not c.lower().endswith(("_id", "_key", "_uuid"))
        ),
        None,
    )

    if cat_col and num_col:
        fallback = (
            f"SELECT {cat_col}, COUNT(*) AS row_count, AVG({num_col}) AS avg_{num_col}\n"
            f"FROM data\n"
            f"GROUP BY {cat_col}\n"
            f"ORDER BY row_count DESC\n"
            f"LIMIT 20;"
        )
        logger.info(
            f"[repair_union_mismatch] Rewrote UNION as simple GROUP BY on "
            f"({cat_col}, {num_col})"
        )
        return fallback

    logger.warning(
        "[repair_union_mismatch] Could not identify suitable columns for fallback"
    )
    return sql


# ---------------------------------------------------------------------------
# [FIX E-05]  Targeted LLM repair prompt
# ---------------------------------------------------------------------------


def build_targeted_repair_prompt(
    original_sql: str,
    error_class: SQLErrorClass,
    columns: list[str],
    original_query: str,
    schema_block: str = "",
) -> str:
    """
    Build a laser-targeted repair prompt for the LLM based on the specific
    error class — NOT a generic "fix your SQL" prompt.

    This is called only when rule-based repair (repair_truncated_sql,
    repair_missing_alias, repair_union_mismatch) either doesn't apply or
    still fails DuckDB validation.
    """
    col_lines = "\n".join(f"  - {c}" for c in columns)
    schema_section = f"\nDATASET SCHEMA:\n{schema_block}\n" if schema_block else ""

    extra_constraints = {
        SQLErrorType.TRUNCATED: (
            "CRITICAL: The previous SQL was cut off before it was complete.\n"
            "Output a SHORT, COMPLETE SQL query. No CTEs. No UNION. No subqueries.\n"
            "Use only SELECT ... FROM data ... GROUP BY ... ORDER BY ... LIMIT 20.\n"
            "Every opening parenthesis must have a closing one.\n"
            "End the SQL with a semicolon."
        ),
        SQLErrorType.MISSING_ALIAS: (
            "CRITICAL: Every column expression in the SELECT list must have an alias.\n"
            "WRONG: COUNT(*) AS\n"
            "RIGHT: COUNT(*) AS row_count\n"
            "Write the alias name BEFORE the closing parenthesis of any subquery or function call."
        ),
        SQLErrorType.UNION_MISMATCH: (
            "CRITICAL: Do NOT use UNION, UNION ALL, INTERSECT, or EXCEPT.\n"
            "All branches of a UNION must have the same number of columns — "
            "this is the error that just failed.\n"
            "Instead: use a single SELECT ... GROUP BY query.\n"
            "If you need to compare groups, use CASE WHEN inside a single SELECT."
        ),
        SQLErrorType.COLUMN_NOT_FOUND: (
            "CRITICAL: Use ONLY column names from the EXACT COLUMN NAMES list below.\n"
            "Do NOT abbreviate, pluralise, or invent column names.\n"
            "If the required column does not exist: SELECT 'Cannot answer: column not found' AS error_message"
        ),
        SQLErrorType.WINDOW_IN_AGGREGATE: (
            "CRITICAL: Window functions cannot be nested inside aggregate functions.\n"
            "Use a CTE pattern:\n"
            "  WITH ranked AS (SELECT col, ROW_NUMBER() OVER (...) AS rn FROM data)\n"
            "  SELECT COUNT(*) FROM ranked WHERE rn = 1\n"
            "Never write SUM(ROW_NUMBER() OVER (...)) or similar."
        ),
        SQLErrorType.GENERIC: "",
    }

    constraint_block = extra_constraints.get(error_class.error_type, "")

    return f"""You are a DuckDB SQL repair agent. One specific error must be fixed.

USER'S QUESTION: {original_query}

EXACT COLUMN NAMES — ONLY THESE ARE VALID:
{col_lines}
{schema_section}
FAILING SQL:
{original_sql}

DUCKDB ERROR:
{error_class.original_error}

REPAIR INSTRUCTION:
{error_class.repair_hint}

{constraint_block}

RULES:
1. Output ONLY valid DuckDB SQL — no explanation, no markdown, no comments.
2. Table name is always: data
3. Use ONLY column names from the EXACT COLUMN NAMES list above.
4. Keep the query as SIMPLE as possible — prefer SELECT ... GROUP BY over CTEs.
5. End with a semicolon.

Return ONLY the corrected SQL.
"""


# ---------------------------------------------------------------------------
# [FIX E-01 + E-02 + E-03 + E-04 + E-05]  The repair agent
# ---------------------------------------------------------------------------


@dataclass
class RepairResult:
    sql: str
    was_repaired: bool
    repair_method: str  # "rule", "llm", "fallback", "none"
    error_class: Optional[SQLErrorClass] = None
    attempt_log: list[str] = field(default_factory=list)


class SQLRepairAgent:
    """
    Self-healing SQL agent that diagnoses DuckDB errors and applies targeted fixes.

    Pipeline per failed SQL:
      1. classify_sql_error()           → identifies the error class
      2. Rule-based repair (no LLM)     → fast, zero cost, covers E-02 and E-03
      3. If rule repair invalid/fails   → build_targeted_repair_prompt() + LLM call
      4. If LLM repair also fails       → simplified fallback SELECT GROUP BY
      5. Log every step for observability
    """

    def __init__(self, llm_router, max_repair_attempts: int = 2):
        self._router = llm_router
        self._max_repair_attempts = max_repair_attempts

    async def repair(
        self,
        sql: str,
        error_msg: str,
        df: pl.DataFrame,
        original_query: str,
        schema_block: str = "",
    ) -> RepairResult:
        attempt_log: list[str] = []
        columns = extract_columns_from_df(df)
        error_class = classify_sql_error(error_msg, sql)

        attempt_log.append(
            f"Classified error as {error_class.error_type.value}: {error_msg[:120]}"
        )
        logger.info(
            f"[SQLRepairAgent] Error classified: {error_class.error_type.value}"
        )

        # Rule-based repair (no LLM cost)
        if error_class.can_rule_repair:
            repaired_sql = self._apply_rule_repair(sql, error_class, columns)
            if repaired_sql != sql:
                is_valid, validation_error = self._quick_validate(repaired_sql)
                if is_valid:
                    attempt_log.append("Rule-based repair succeeded")
                    logger.info("[SQLRepairAgent] Rule-based repair succeeded")
                    return RepairResult(
                        sql=repaired_sql,
                        was_repaired=True,
                        repair_method="rule",
                        error_class=error_class,
                        attempt_log=attempt_log,
                    )
                else:
                    attempt_log.append(
                        f"Rule repair produced invalid SQL: {validation_error} — escalating to LLM"
                    )
                    logger.warning(
                        f"[SQLRepairAgent] Rule repair invalid: {validation_error}"
                    )

        # LLM-guided repair
        for llm_attempt in range(1, self._max_repair_attempts + 1):
            attempt_log.append(f"LLM repair attempt {llm_attempt}")
            logger.info(
                f"[SQLRepairAgent] LLM repair attempt {llm_attempt}/{self._max_repair_attempts}"
            )
            try:
                prompt = build_targeted_repair_prompt(
                    original_sql=sql,
                    error_class=error_class,
                    columns=columns,
                    original_query=original_query,
                    schema_block=schema_block,
                )
                repaired_sql = await self._router.call(
                    prompt=prompt,
                    model_role="sql_generator",
                    expect_json=False,
                    temperature=0.0,
                    max_tokens=600,
                )
                repaired_sql = self._clean_sql(repaired_sql)
                is_valid, validation_error = self._quick_validate(repaired_sql)

                if is_valid:
                    attempt_log.append(f"LLM repair attempt {llm_attempt} succeeded")
                    logger.info(
                        f"[SQLRepairAgent] LLM repair succeeded on attempt {llm_attempt}"
                    )
                    return RepairResult(
                        sql=repaired_sql,
                        was_repaired=True,
                        repair_method="llm",
                        error_class=error_class,
                        attempt_log=attempt_log,
                    )
                else:
                    attempt_log.append(
                        f"LLM repair {llm_attempt} invalid: {validation_error}"
                    )
                    logger.warning(
                        f"[SQLRepairAgent] LLM repair {llm_attempt} invalid: {validation_error}"
                    )

            except Exception as e:
                attempt_log.append(f"LLM repair {llm_attempt} exception: {e}")
                logger.error(
                    f"[SQLRepairAgent] LLM repair {llm_attempt} exception: {e}",
                    exc_info=True,
                )

        # Simplified fallback SQL
        fallback = self._build_fallback_sql(columns, original_query)
        attempt_log.append(
            f"All {self._max_repair_attempts} LLM repair attempts failed — "
            f"using simplified fallback SQL"
        )
        logger.warning(
            f"[SQLRepairAgent] All repairs failed for error: {error_msg[:80]}. "
            f"Using fallback SQL."
        )
        return RepairResult(
            sql=fallback,
            was_repaired=True,
            repair_method="fallback",
            error_class=error_class,
            attempt_log=attempt_log,
        )

    def _apply_rule_repair(
        self,
        sql: str,
        error_class: SQLErrorClass,
        columns: list[str],
    ) -> str:
        if error_class.error_type == SQLErrorType.TRUNCATED:
            return repair_truncated_sql(sql)
        if error_class.error_type == SQLErrorType.MISSING_ALIAS:
            return repair_missing_alias(sql)
        if error_class.error_type == SQLErrorType.UNION_MISMATCH:
            return repair_union_mismatch(sql, columns)
        return sql

    def _clean_sql(self, raw: str) -> str:
        sql = raw.strip()
        if sql.startswith("```"):
            lines = sql.split("\n")
            sql = "\n".join(lines[1:-1] if lines[-1].strip() == "```" else lines[1:])
        sql = sql.strip().rstrip(";") + ";"
        return sql

    def _quick_validate(self, sql: str) -> tuple[bool, str]:
        if not sql or not sql.strip():
            return False, "Empty SQL"

        sql_upper = sql.upper().strip()

        forbidden = [
            r"\bDROP\b",
            r"\bDELETE\b",
            r"\bTRUNCATE\b",
            r"\bINSERT\b",
            r"\bUPDATE\b",
            r"\bALTER\b",
        ]
        for pat in forbidden:
            if re.search(pat, sql_upper):
                return False, f"Forbidden operation: {pat}"

        if not (sql_upper.startswith("SELECT") or sql_upper.startswith("WITH")):
            return False, "Must start with SELECT or WITH"

        if sql.count("(") != sql.count(")"):
            return (
                False,
                f"Unbalanced parentheses: {sql.count('(')} open vs {sql.count(')')} close",
            )

        if re.search(
            r"\bAS\s+(?:FROM|WHERE|GROUP|ORDER|LIMIT|;?\s*$)", sql, re.IGNORECASE
        ):
            return False, "Dangling AS keyword with no alias name"

        return True, ""

    def _build_fallback_sql(self, columns: list[str], query: str) -> str:
        cat_col = next(
            (
                c
                for c in columns
                if not any(c.lower().endswith(s) for s in ("_id", "_key", "_uuid"))
            ),
            columns[0] if columns else "rowid",
        )
        fallback = (
            f"SELECT {cat_col}, COUNT(*) AS row_count\n"
            f"FROM data\n"
            f"GROUP BY {cat_col}\n"
            f"ORDER BY row_count DESC\n"
            f"LIMIT 20;"
        )
        logger.info(f"[SQLRepairAgent] Fallback SQL built using column: {cat_col}")
        return fallback


